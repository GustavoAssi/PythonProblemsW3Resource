def boolean_to_integer_converter() -> None:
    """
    This program converts true to 1 and false to 0.
    Author: Gustavo Assi Alencar.
    Date:   24/11/2025.
    """

    # Step 1: Just use int() function to convert.
    print(f"Boolean: {True} -> int: {int(True)}")
    print(f"Boolean: {False} -> int: {int(False)}")


def main():
    boolean_to_integer_converter()


if __name__ == "__main__":
    main()
